package introex;

public class Unicode {
    public static void main(String[] args) {
        char ch; ch = 'X';
        System.out.println("ch contem " + ch);

        ch++; // incrementa ch
        System.out.println("agora ch é: " + ch);

        ch = 90; // da o valor Z para ch
        System.out.println("agora ch é: " + ch);
    }
}
